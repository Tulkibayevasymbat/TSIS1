#PUTHON CASTING

#Example integer
x = int(1)
y = int(2.8)
z = int("3")      #output:
print(x)          #1
print(y)          #2
print(z)          #3


#Example floats
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")  #output:
print(x)          #1.0
print(y)          #2.8
print(z)          #3.0
print(w)          #4.2


#Example strings
x = str("s1")
y = str(2)
z = str(3.0)      #output:
print(x)          #s1
print(y)          #2
print(z)          #3.0


